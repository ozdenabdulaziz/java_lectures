package day06_AritmeticOperators;

public class ArithmeticOperators {

    public static void main(String[] args) {

        System.out.println(10/4); // 2
        System.out.println("10/4"); // text

        System.out.println(12 + 3.0); // 15.0

        System.out.println("12" + 3); // text 3 ==> 123, concatenation 123

        System.out.println('a' + 3);    //100 addition, every single char has a corresponding number ascci table
                        //  97 on the ascii table

        System.out.println(12.0 - 4); // 8.0 double result

        double a = 12.0 / 3 ;
        System.out.println(a); // 4.0 double result

        int b = 10/3 ;
        System.out.println(b); // int result

        double c = 10 /3 ;
        System.out.println(c); // double result
        //              c = 3
        //                 3.0

        double d = 10/3.0;
        System.out.println(d);     //          d = 3.333...


        int x = 3/2;
        System.out.println(x); // x = 1

        double y = 3/2;
        System.out.println(y);  // y = 1.0

        double z = 3.0/2;
        System.out.println(z);

        System.out.println("\n ============================================================ \n");

        System.out.println(100%13);
        System.out.println(100%10);

        System.out.println( 20 % 4.5);





    }

}
