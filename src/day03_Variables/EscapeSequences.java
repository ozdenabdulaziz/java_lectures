package day03_Variables;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello Everyone");
        System.out.println("My name is Abdulaziz, I am from Windsor.");

        System.out.println("===========================");

        System.out.println("Hello Wveryone \nMy name is Abdulaziz, I am from Windsor.");
                        // We do not use space between \n and My name is because we begin a new line.


        // \t:

        System.out.println("\tToday I learned Java");
        // after \t thats give you a paragraph space.
        System.out.println("It was so much fun.");



        // \\= \ double back slash

        System.out.println("/"); // prints front slash
        System.out.println("\\"); // prints single back slash

        System.out.println("======================================");

        System.out.println("My name is 'Abdulaziz'");
        System.out.println("My name is \'Abdulaziz\'");

        System.out.println("=======================================");
        // \"
        System.out.println("My favorite show is Game of Thrones");
        System.out.println("My favorite show is \"Game of Thrones\"");




    }



}

