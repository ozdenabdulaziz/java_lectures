package day03_Variables;

public class Print_Vs_Println {

    public static void main(String[] args) {

        System.out.println("Hello");
        System.out.println("Hello");
        System.out.println("Hello");

        System.out.println("==============================");

        System.out.print("Hello, ");
        System.out.print("Hello, ");
        System.out.print("Hello. ");
        System.out.println("How are you"); // this line with 15th line will be same line
        System.out.println("Are you okay"); // this line is will be new line.
        System.out.print("Yes I am,"); // this line will be new line. but next line will be same line with this line.
        System.out.println(" How about you"); // this line will be same with 18th line.

        System.out.println("=============================");

        System.out.println("Hello Everyone, I am learning Java, Java is a programing language and it's so much fun, easy to learn, I am in love with Java.");





    }


}
